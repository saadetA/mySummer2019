package oOPCalisma;

public class Insan {

private String name;
private String lastName;
private int Age;
	
	public Insan(String name,String lastName,int Age) {
		 this.name=name;
	     this.lastName=lastName;
	     this.Age=Age;
	}
	public void setName(String name) {
		this.name=name;
	}
public String getName() {
	return name;
}
public void setLastname(String lastname) {
	this.lastName=lastname;
}
public String getLastName() {
	return lastName;
}

public void setAge(int Age) {
	this.Age=Age;
}

public int getAge() {
	return Age;
}




}	
	
	










